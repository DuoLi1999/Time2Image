def get_gmat(m,n,v,num):
    field = 3
    stride =field /8
    X = np.arange(-field, field, stride)
    Y = np.arange(-field, field, stride)
    x, y = np.meshgrid(X, Y)
    pos = np.empty(x.shape + (2,))
    pos[:, :, 0] = x
    pos[:, :, 1] = y
    # var = (radius[str(num)]*4*v)*(radius[str(num)]*4*v)
    var = radius[str(num)]**2
    Sigma = [[var,0],
             [0,var]]
    mu = [m,n]
    z = multivariate_normal(mu, Sigma)
    pdf = np.array(z.pdf(pos))
    pdf = (pdf - np.min(pdf)) / (np.max(pdf) - np.min(pdf))
    return pdf

