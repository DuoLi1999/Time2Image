def read_arrf(dir_data):
    data, meta = arff.loadarff(dir_data)
    field_data = [list(data[field]) for field in data.dtype.names]
    labels = field_data[1]
    label_encoder = LabelEncoder()
    numeric_labels = label_encoder.fit_transform(labels)
    dataset = np.array([x.tolist() for x in field_data[0]])
    return dataset, numeric_labels.tolist()
def get_loader(args):
    if args.local_rank not in [-1, 0]:
        torch.distributed.barrier()

    transform_train = transforms.Compose([
        transforms.Resize((args.img_size, args.img_size)),
        # transforms.RandomHorizontalFlip(),
        transforms.RandomHorizontalFlip(),  # 随机水平翻转
        transforms.ColorJitter(brightness=0.4, contrast=0.4, saturation=0.4),
        transforms.ToTensor()
    ])
    transform_test = transforms.Compose([
        transforms.Resize((args.img_size, args.img_size)),
        transforms.ToTensor()
    ])
    name = args.name
    data_folder = os.path.join('Multivariate', name)
    dir_train_x = os.path.join(data_folder, 'train_x.npy')
    dir_train_y = os.path.join(data_folder, 'train_y.npy')
    dir_test_x = os.path.join(data_folder, 'test_x.npy')
    dir_test_y = os.path.join(data_folder, 'test_y.npy')

    print('---------------------Dataloading---------------------')
    # data_train,train_y =  read_arrf(dir_trian)
    # data_test, test_y = read_arrf(dir_test)
    data_train = np.load(dir_train_x)
    train_y = np.load(dir_train_y)
    data_test = np.load(dir_test_x)
    test_y = np.load(dir_test_y)
    train_x = []
    for i in tqdm(data_train):
        img = getImage(i,args.v)
        train_x.append(img)
    
    test_x = []
    for i in tqdm(data_test):
        img = getImage(i,args.v)
        test_x.append(img)

    
    trainset = ImageDataset(train_x,train_y,transform=transform_train)
    testset = ImageDataset(test_x,test_y,transform=transform_test)

    
    if args.local_rank == 0:
        torch.distributed.barrier()

    train_sampler = RandomSampler(trainset) if args.local_rank == -1 else DistributedSampler(trainset)
    test_sampler = SequentialSampler(testset)
    train_loader = DataLoader(trainset,
                              sampler=train_sampler,
                              batch_size=args.train_batch_size,
                              num_workers=0,
                              pin_memory=True)
    test_loader = DataLoader(testset,
                             sampler=test_sampler,
                             batch_size=args.eval_batch_size,
                             num_workers=0,
                             pin_memory=True) if testset is not None else None
    
    return train_loader, test_loader
