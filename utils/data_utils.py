def get_new_seq(speed,size):
    seq = np.nan_to_num(speed, nan=0)
    target_length = size
    x_new = np.linspace(0, len(seq)-1, target_length)

    f = interp1d(np.arange(len(seq)), seq, kind='cubic')
    seq_new = f(x_new)
    return seq_new
