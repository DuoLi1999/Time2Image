def get_patch(speed,v):
    num = speed.shape[0]
    F = np.ones((num, 196, 16, 16))
    pdf_list=[]
    for i in range(num):
        pdf_list.append(get_gmat(12*mat_pos[str(num)][i][0],12*mat_pos[str(num)][i][1],v,num))
    trans_speed = np.transpose(speed)
    Q = np.ones((num,224,224))
    for k in range(num):
        F[k] =(trans_speed[:,k,np.newaxis,np.newaxis]*pdf_list[k])
        rows, cols = 14, 14
        As = []
        for i in range(rows):
            A_row = np.hstack(F[k][i * cols:(i + 1) * cols])
            As.append(A_row)

        Q[k] = np.vstack(As)
    for i in range(num):
        Q[i] -= Q[i].mean()
    L = np.sum(Q, axis=0)
    F = (L - np.min(L)) / (np.max(L) - np.min(L)) * 255
    return F

def getImage(x,v):
    if x.shape[0]> 256:
        new_x = np.transpose(x)
        # x_t = np.transpose(x)
        # pca = PCA(n_components=270)
        # x_pca = pca.fit_transform(x_t)
        # new_x = np.transpose(x_pca)
        # new_x = x
    else:
        new_x = x
    size =new_x.shape[0]
    speed=np.zeros((size,196))
    for i in range(size):
        speed[i]=get_new_seq(new_x[i],196)

    for i in range(size):
        mu0 = np.mean(speed[i])
        sigma0 = np.sqrt(np.var(speed[i]))
        speed[i] = (speed[i] - mu0) / sigma0
        speed[i] -= speed[i].mean()
    F =get_patch(speed,v)

    img = Image.fromarray((F).astype(np.uint8), mode='L')

    return img   

class ImageDataset(Dataset):
    def __init__(self, data, labels, transform=None):
        self.data = data
        self.labels = labels
        self.transform = transform

    def __len__(self):
        return len(self.data)

    def __getitem__(self, index):
        image = self.data[index]
        if image.mode != 'RGB':
            image = image.convert('RGB')
        if self.transform is not None:
            image = self.transform(image)
        label = self.labels[index]
        return image, label
